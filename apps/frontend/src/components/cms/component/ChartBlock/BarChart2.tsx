const actions = [
  {
    name: "Randomize",
    handler(chart) {
      chart.data.datasets.forEach((dataset) => {
        dataset.data = Utils.numbers({
          count: chart.data.labels.length,
          min: -100,
          max: 100,
        });
      });
      chart.update();
    },
  },
  {
    name: "Add Dataset",
    handler(chart) {
      const data = chart.data;
      const dsColor = Utils.namedColor(chart.data.datasets.length);
      const newDataset = {
        label: "Dataset " + (data.datasets.length + 1),
        backgroundColor: Utils.transparentize(dsColor, 0.5),
        borderColor: dsColor,
        borderWidth: 1,
        data: Utils.numbers({ count: data.labels.length, min: -100, max: 100 }),
      };
      chart.data.datasets.push(newDataset);
      chart.update();
    },
  },
  {
    name: "Add Data",
    handler(chart) {
      const data = chart.data;
      if (data.datasets.length > 0) {
        data.labels = Utils.months({ count: data.labels.length + 1 });

        for (let index = 0; index < data.datasets.length; ++index) {
          data.datasets[index].data.push(Utils.rand(-100, 100));
        }

        chart.update();
      }
    },
  },
  {
    name: "Remove Dataset",
    handler(chart) {
      chart.data.datasets.pop();
      chart.update();
    },
  },
  {
    name: "Remove Data",
    handler(chart) {
      chart.data.labels.splice(-1, 1); // remove the label first

      chart.data.datasets.forEach((dataset) => {
        dataset.data.pop();
      });

      chart.update();
    },
  },
];
export const BarChart2 = () => {
  const chartRef = useRef<HTMLCanvasElement>(null);
  const [chartInstance, setChartInstance] = useState<Chart | null>(null);
  const [range, setRange] = useState("1Y");
  const [showJson, setShowJson] = useState(false);

  // Time range configurations
  const ranges = {
    "1M": 30,
    "3M": 90,
    "6M": 180,
    YTD: Math.floor(
      (new Date().getTime() -
        new Date(new Date().getFullYear(), 0, 1).getTime()) /
        (1000 * 60 * 60 * 24),
    ),
    "1Y": 365,
    "5Y": 365 * 5,
    "10Y": 365 * 10,
  };

  // Process data for chart and JSON
  const processData = (days: number) => {
    const today = new Date();
    const cutoffDate = new Date(today.getTime() - days * 24 * 60 * 60 * 1000);
    return rawData
      .filter(([timestamp]) => timestamp >= cutoffDate.getTime())
      .map(([timestamp, price]) => ({
        date: new Date(timestamp).toISOString().split("T")[0],
        price: parseFloat(price.toFixed(2)),
      }));
  };

  // Get JSON data as string
  const getJsonData = () => {
    const data = processData(ranges[range]);
    return JSON.stringify(data, null, 2);
  };

  useEffect(() => {
    if (!chartRef.current) return;

    const data = processData(ranges[range]);
    const labels = data.map((d) => d.date);
    const prices = data.map((d) => d.price);

    if (chartInstance) {
      chartInstance.destroy();
    }

    const ctx = chartRef.current.getContext("2d");
    if (!ctx) return;

    const newChart = new Chart(ctx, {
      type: "bar",
      data: {
        labels: labels,
        datasets: [
          {
            label: "Dataset 1",
            data: Utils.numbers(NUMBER_CFG),
            borderColor: Utils.CHART_COLORS.red,
            backgroundColor: Utils.transparentize(Utils.CHART_COLORS.red, 0.5),
          },
          {
            label: "Dataset 2",
            data: Utils.numbers(NUMBER_CFG),
            borderColor: Utils.CHART_COLORS.blue,
            backgroundColor: Utils.transparentize(Utils.CHART_COLORS.blue, 0.5),
          },
        ],
      },
      options: {
        responsive: true,
        plugins: {
          legend: {
            position: "top",
          },
          title: {
            display: true,
            text: "as of 12/31/2024",
          },
        },
        scales: {
          y: {
            beginAtZero: false,
          },
        },
      },
    });

    setChartInstance(newChart);

    return () => newChart.destroy();
  }, [range]);

  return (
    <div className="max-w-4xl mx-auto">
      <div className="mb-4 flex space-x-2">
        {Object.keys(ranges).map((r) => (
          <button
            key={r}
            onClick={() => setRange(r)}
            className={`px-3 py-1 rounded ${
              range === r
                ? "bg-verdansk text-white"
                : "bg-gray-200 text-gray-800"
            } hover:bg-blue-500 hover:text-white transition`}
          >
            {r}
          </button>
        ))}
        <button
          onClick={() => setShowJson(!showJson)}
          className="px-3 py-1 rounded bg-main text-[rgba(248,248,25,0)]"
        >
          {showJson ? "Hide JSON" : "Show JSON"}
        </button>
      </div>
      {showJson && (
        <div className="mb-4">
          <textarea
            className="w-full h-64 p-2 border rounded font-mono text-sm"
            value={getJsonData()}
            readOnly
          />
        </div>
      )}
      <div className="h-96">
        <canvas ref={chartRef}></canvas>
      </div>
    </div>
  );
};
